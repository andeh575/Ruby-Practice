# Some variables for printing a 'joke'
typesOfPeople = 10
binary = "binary"
doNot = "don't"
x = "There are #{typesOfPeople} types of people."
y = "Those who understand #{binary} and those who #{doNot}."

# Run the joke
puts x
puts y

# Struggling for appreciation
hilarious = false
jokeEvaluation = "Isn't that joke so funny?! #{hilarious}"

puts jokeEvaluation

# A final combination of two strings
w = "This is the left side of... "
e = "a string with a right side."

puts w + e
